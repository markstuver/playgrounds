import UIKit

// While Loops run a bit of code over and over until it's comparison fails.

var x = 5

// In While Loop: Condition is tested first and then the statements are executed if the condition turns out to be true

while x < 10 {
    println(x)
    x++
}



var y = 10


// Do While Loop: Statements are executed for the first time and then the conditions are tested, if the condition turns out to be true, then the statements are executed again.
do {
    println(y)
    y--
    
} while y > 0


